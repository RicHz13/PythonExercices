#"isinstance" y "hasattr"
#Los métodos son como funciones que tienen sentido únicamente en el contexto de una clase
#Al igual que las varibles, los métodos privados se definen con un guión bajo
#La "ENCAPSULACIÓN" es un concepto clave de la progración orientada a objetos.
    #la practica de aplicar este principio es declarar todas las variables y métodos como privados,
